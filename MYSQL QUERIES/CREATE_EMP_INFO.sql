	ID INT NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(40),
    EMAIL VARCHAR(40),
    PHONE INT,
    CITY VARCHAR(40),
    WORKING_HOURS INT,
    PRIMARY KEY(ID)
);
    
